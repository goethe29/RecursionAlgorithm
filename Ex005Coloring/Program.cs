        static void Coloring(int x, int y)
        {
            if(bitPicture[x,y] == 0)
            {
                bitPicture[x, y] = 2;

                Paint(50, 50, 10);
                Console.ReadLine();

                Coloring(x-1, y);
                Coloring(x, y-1);
                Coloring(x+1, y);
                Coloring(x, y + 1);
               
            }
        }

        static void Paint(int StartPosX, int StartPosY, int Scale)
        {
            GraphicsWindow.Clear();
            for (int i = 0; i < bitPicture.GetLength(0); i++)
            {
                for (int j = 0; j < bitPicture.GetLength(1); j++)
                {
                    if (bitPicture[i, j] == -1) GraphicsWindow.BrushColor = "Red";
                    else if (bitPicture[i, j] == 2) GraphicsWindow.BrushColor = "Blue";
                    else if (bitPicture[i, j] == 0) GraphicsWindow.BrushColor = "White";

                    GraphicsWindow.FillRectangle(
                            x: StartPosX + (j - Scale / 2) * Scale,
                            y: StartPosY + (i - Scale / 2) * Scale,
                            width: Scale,
                            height: Scale);
                }
            }
        }
         
        static void Main()
        {
            GraphicsWindow.Show();
            
            GraphicsWindow.MouseDown += () =>
            {
                Coloring(10, 10);
                Paint(550, 50, 10);
            };
            Paint(50, 50, 10);
        }

        
